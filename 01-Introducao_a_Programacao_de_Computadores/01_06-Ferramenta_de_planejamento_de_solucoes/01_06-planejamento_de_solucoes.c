/*
Pseudocódigos e fluxogramas

São ferramentas que auxiliam a planejar os códigos, diminuindo o nível de abstração necessária,
uma vez que permitem focar na lógica de programação ao invés das particularidades da linguagem de
programação.

Pseudocódigos utilizam-se da própria linguagem nativa do usuário, como a lingua portuguesa, 
enquanto que os fluxogramas são representações gráficas do funcionamento de determinado código.

PSeInt e Portugol são algumas opções para a criação e organização de pseudocódigos;


Fluxogramas utilizam-se de algumas figuras específicas como:
    - Retângulo - Ação ou processo: indica uma operação ou conjunto de operações;
    - Losango - Decisão: indica um ponto onde uma decisão é tomada, levando à diferentes caminhos;
    - Círculo - Conector: serve para conectar diferentes partes do fluxograma;
    - Paralelogramo - Entrada e Saída de Dados: indica pontos em que dados são recebidos/enviados;
    - Oval - Início/Fim: Indica o começo/fim do fluxo;
    - Seta de fluxo: indica a direção/sequência do fluxo do processo;
    - Triângulo: indica uma operação de extração de dados/informações.
    

De um modo geral, essas ferramentas podem beneficiar o planejamento da construção de um programa,
garantindo clareza e fácil visualização dos programas, boa visualização da lógica do programa e 
corroboram com os conceitos de solução estruturada vistos na aula anterior.
*/